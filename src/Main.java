    public static int valorDecimal(String bin) throws NumberFormatException{
        int resp = 0;
        for(int i = 0; i < bin.length(); i++){
            if(bin.charAt(i) == '1')
                resp += Math.pow(2, bin.length() - i - 1);
            else if(bin.charAt(i) != '0')
                throw new NumberFormatException();
        }
        return resp;
    }
    
    private void campoBin2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_campoBin2KeyReleased
        try{
            String bin = campoBin2.getText();
            boolean negativo = bin.startsWith("1");
            int valor = (negativo ? -1 : 1);
            if(negativo)
                bin = complementa(bin);
            valor *= valorDecimal(bin);
            campoDec2.setText(valor + "");
            labelBintoDec.setText("Status: convertido com sucesso.");
            campoBin2.setBorder(original);
        } catch(NumberFormatException e){
            campoDec2.setText("");
            labelBintoDec.setText("Status: erro na conversão.");
            campoBin2.setBorder(bordaVermelha);
        }
    }//GEN-LAST:event_campoBin2KeyReleased

