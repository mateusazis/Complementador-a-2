    public static String complementa(String n){
        String resp = "";
        boolean achou1 = false;
        for(int i = n.length() - 1; i >= 0; i--){
            char atual = n.charAt(i);
            if(!achou1)
                resp = atual + resp;
            else
                resp = (atual == '1' ? '0' : '1') + resp;
            achou1 = achou1 || atual == '1';
        }
        return resp;
    }

    private void converter(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_converter
        try{
            int n = Integer.parseInt(campoNumero.getText());
            int bits = Integer.parseInt(campoBits.getText());
            String bin = '0' + Integer.toBinaryString(Math.abs(n));
            if(bits != 0 && bin.length() > bits)
                throw new NumberFormatException("overflow");
            labelOverflow.setText("Status: sem overflow!");
            while(bin.length() < bits)
                bin = '0' + bin;
            String c2 = complementa(bin);
            campoBin.setText(bin);
            campoBinC2.setText(c2);
            if(bordaAlterada){
                campoBits.setBorder(original);
                campoNumero.setBorder(original);
                bordaAlterada = false;
            }
        }catch(NumberFormatException e){
            campoBin.setText("");
            campoBinC2.setText("");
            String msg = e.getMessage();
            if(msg.equals("overflow")){
                labelOverflow.setText("Status: houve overflow!");
                campoBits.setBorder(bordaVermelha);
            }
            else{
                labelOverflow.setText("Status: erro no formato do número!");
                campoBits.setBorder(original);
                campoNumero.setBorder(bordaVermelha);
            }
            bordaAlterada = true;
        }
}//GEN-LAST:event_converter

